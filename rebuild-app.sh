#!/bin/bash

echo "=== Пересборка приложения ==="

# Остановка контейнеров
echo "1. Остановка контейнеров..."
docker-compose down

# Пересборка приложения
echo "2. Пересборка приложения..."
docker-compose up -d --build app

# Ожидание запуска
echo "3. Ожидание запуска..."
sleep 10

# Проверка статуса
echo "4. Проверка статуса..."
docker-compose ps

echo "5. Проверка логов..."
docker-compose logs --tail=20 app

echo ""
echo "=== Приложение обновлено! ==="
echo "Сайт доступен по адресу: http://37.233.85.193:3000"
